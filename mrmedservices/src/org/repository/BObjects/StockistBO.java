package org.repository.BObjects;


public class StockistBO {
	

	private String stockistID;
	private String name;
	private String licNu;
	private String add;
	private String contNu;
	private String contPer;
	private String email;
	public String getStockistID() {
		return stockistID;
	}
	public void setStockistID(String stockistID) {
		this.stockistID = stockistID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLicNu() {
		return licNu;
	}
	public void setLicNu(String licNu) {
		this.licNu = licNu;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	public String getContNu() {
		return contNu;
	}
	public void setContNu(String contNu) {
		this.contNu = contNu;
	}
	public String getContPer() {
		return contPer;
	}
	public void setContPer(String contPer) {
		this.contPer = contPer;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

	
}
