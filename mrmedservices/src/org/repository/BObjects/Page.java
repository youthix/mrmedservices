package org.repository.BObjects;

public class Page {
	
	public String id;
	public String fbIds;
	public String table;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFbIds() {
		return fbIds;
	}
	public void setFbIds(String fbIds) {
		this.fbIds = fbIds;
	}
	public String getTable() {
		return table;
	}
	public void setTable(String table) {
		this.table = table;
	}


}
